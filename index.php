  <p><h2>LNU History</h2></p>
  <hr>
<p><b>The Beginning</b></p>
<p>1969:</p>

  <p>
Lyceum- Northwestern University was found by Dr. Fransico Quimson Doque and Mrs Florencia Tiongson Duque
</p>

<p><b>The merger</b></p>
<p>1974:</p>

  <p>
Acquisition of the Northwestern Educational Institution
</p>
<p>
From the merger emerged the name Lyceum Northwestern (LN)
</p>
<p>
New courses: B.S. Elemtary Education, Junior Secretarial and Master of Arts in Education
</p>

<p><b>The Establishment of the First Medical School of the North</b></p>
<p>
Lyceum Northwestern led a consortium of local tertiary schools and founded the Northern Philippines Institute of Medicine. LN became the "first medical school of the north" which began with an enrolment of 19 students
</p>

<p>2001:</p>
<p>Inauguration of the LN Urdaneta Campus on June 15
</p>

<p>2002:</p>
<p>Acquisition of the Asia Career College housing L-NU's College of Law, Midwifery,Voctechand Caregiver Course
</p>

<p>2003:</p>
<p>LNU earned the ISO Certificate 9001:2000 (November 25)
</p>
<p>L-NU as an Outstanding Institution of Learning given in Manila by the Philippine Regulations Commission.</p>

<p>2004:</p>
<p>LNU sends Tourism and Engineering graduating students to Singapore for their on-the-job training
</p>
<p>The College of Nursing LNU's biggest department, produced a seventh placer in the national board examination</p>

<p>2006:</p>
<p>L-NU is delighted to received the Top No. 3 School Award for it College of Pharmacy as provided by the Philippine Regulatory Commission
(PRC) for garnering 92 percent in the 2006 National Board Examinations.
</p>
<p>2007:</p>
<p>The College of Tourism sent five (5) students to the United States of America for their OJT
</p>
<p>2008:</p>
<p>LNU,through its Research Development and Management Office is the first HEI able to publish in the ACADEME: Journal of Leadership and Management in Higher Education, a refered journal published by the Internation Association of University Presidents based at SIAM University,Bangkok, Thailand
</p>
<p>2009:</p>
<p>L-NU signs Memorandum of Agreement with the CHED Zonal Research Center (Region 1 and CAR) for the conduct of a Php 825,000 full grant research on the "Matching Teaching Styles of Instructors with the Learning Styles of Students: Its Relation to Academic Performance of Teacher Education Institutions on Region 1 and CAR."
</p>
<p>2010:</p>
<p>L-NU was granted its renewal of Deregulated Status by the Commision on Higher Education
</p>
<p>2011:</p>
<p>The newly renovated and Enhanced Gymnasium was dedicated in honor of the late Dr.Salvador T. Duque  and was then named as the "Dr.Salvador T. Duque Fieldhouse"
</p>



